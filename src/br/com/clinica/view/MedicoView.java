    public void listar() {
        MedicoDAO dao = new MedicoDAO();
        List<Medico> lista = dao.listarMedicos();
        DefaultTableModel dados = (DefaultTableModel) tbl_medicos.getModel();
        dados.setNumRows(0);
        for (Medico m : lista) {
            dados.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getCrm(),
                m.getIdade(),
                m.getEspecialidade(),
                m.getTelefone(),
                m.getEndereco(),
                m.getEmail()
            });
        }
    }

